#include <stdio.h> // Import standard input/output library so we can use printf() function.

// Basic main() function signature. The application entry point.
// This one takes no arguments (void).
// Therefore, command-line arguments won't be passed to the program.
int main(void) {

  printf("hello, world\n"); // Print a string literal followed by a new line character.
  return 0; // Return 0 from main to exit the program. 0 indicates success. 1 indicates failure.

}

