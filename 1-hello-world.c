// Import standard input/output library
// so we can use printf() function.
#include <stdio.h>

// Basic main() function signature.
// The application entry point.
// This one takes no arguments (void).
// Therefore, command-line arguments
// won't be passed to the program.
int main(void) {

  // Print a string literal
  // followed by a new line character.
  printf("hello, world\n");
  
  // Return 0 from main to exit the program.
  // 0 indicates success. 1 indicates failure.
  return 0;

}

